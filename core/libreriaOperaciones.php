/**
 * @function sumar()
 * @param $num1 Primer valor
 * @param $num2 Segundo valor
 * @return La suma de los valores recibidos como parámetro
 */
function sumar($num1, $num2) {
    return $num1 + $num2;
}

/**
 * @function restar()
 * @param $num1 Primer valor
 * @param $num2 Segundo valor
 * @return La resta de los valores recibidos como parámetro
 */
function restar($num1, $num2) {
    return $num1 - $num2;
}

/**
 * @function multiplicar()
 * @param $num1 Primer valor
 * @param $num2 Segundo valor
 * @return El producto de los valores recibidos como parámetro
 */
function multiplicar($num1, $num2) {
    return $num1 * $num2;
}

/**
 * @function dividir()
 * @param $num1 Primer valor
 * @param $num2 Segundo valor
 * @return El cociente de los valores recibidos como parámetro
 */
function dividir($num1, $num2) {
    return $num1 / $num2;
}